package com.app.ivko;

class TimeContainer {
    private final String MORNING_TIME = "06:00:00";
    private final String DAY_TIME = "09:00:00";
    private final String EVENING_TIME = "19:00:00";
    private final String NIGHT_TIME = "23:00:00";

    public String getMORNING_TIME() {
        return MORNING_TIME;
    }

    public String getDAY_TIME() {
        return DAY_TIME;
    }

    public String getEVENING_TIME() {
        return EVENING_TIME;
    }

    public String getNIGHT_TIME() {
        return NIGHT_TIME;
    }
}